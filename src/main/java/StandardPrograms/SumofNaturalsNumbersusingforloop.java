package StandardPrograms;

public class SumofNaturalsNumbersusingforloop {
    public static void main(String[] args) {
        int i,num=10,sum=0;
        for(i=0;i<=num;++i)
        {
            sum=sum+i;
            System.out.println("sum of first 10Natural Numbers is="+sum);
        }
    }
}